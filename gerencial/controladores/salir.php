    public function __construct()
    {
        $this->ValidarSesion();
    }
    
    /*============================================================================
	 *
	 *	Destructor
	 *
    ============================================================================*/
    public function __destruct()
    {
        
    }

    /*============================================================================
	 *
	 *	
