<?php

/*================================================================================
 *--------------------------------------------------------------------------------
 *
 *	Formato
 *
 *--------------------------------------------------------------------------------
================================================================================*/
class Formato
{
	/*============================================================================
	 *
	 *	Numero
	 *
	============================================================================*/
	public static function Numero($numero, $decimales = 0)
	{
		$salida = number_format($numero, $decimales, ",", ".");
		return $salida;
	}

	/*============================================================================
	 *
	 *	Clave
	 *
	============================================================================*/
	public static function Clave($text)
	{
		$salida = "";
		for($I=0; $I<strlen($text); $I++)
		{
			$salida .= "*";
		}

		return $salida;
	}

	/*============================================================================
	 *
	 *	Boolean to Text
	 *
	============================================================================*/
	public static function bool2text($valBool)
	{
		$salida = "";

		if($valBool) {

			$salida = "Si";

		} else {

			$salida = "No";

		}

		return $salida;
	}
}