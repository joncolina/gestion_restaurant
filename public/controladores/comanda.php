<?php

/*================================================================================
 *--------------------------------------------------------------------------------
 *
 *	Controlador del COMANDA
 *
 *--------------------------------------------------------------------------------
================================================================================*/
class Controlador extends ControladorBase
{
    /*============================================================================
	 *
	 *	Constructor
	 *
    ============================================================================*/
    public function __construct()
    {
        $this->ValidarSesion();

        if( !Peticion::getEsAjax() )
        {
            Incluir::Template("modelo_cliente");
            Template::Iniciar();
        }
    }
    
    /*============================================================================
	 *
	 *	Destructor
	 *
    ============================================================================*/
    public function __destruct()
    {
        if(!Peticion::getEsAjax()) {
            Template::Finalizar();
        }
    }

    /*============================================================================
	 *
	 *	
	 *
    ============================================================================*/
    public function index()
    {
        $this->CSS("ajustes");
        $this->Vista("comanda/index");
        $this->Javascript("comanda/index");
    }

    /*============================================================================
	 *
	 *	
	 *
    ============================================================================*/
    public function consultar()
    {
        $this->AJAX("comanda/consultar");
    }

    /*============================================================================
	 *
	 *	
	 *
    ============================================================================*/
    public function pedidos()
    {
        $this->AJAX("comanda/pedidos");
    }
}