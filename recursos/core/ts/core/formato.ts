/*================================================================================
 *--------------------------------------------------------------------------------
 *
 *	Formato
 *
 *--------------------------------------------------------------------------------
================================================================================*/
class Formato
{
    /*============================================================================
	 *
	 *	Numerico
	 *
	============================================================================*/
    public static Numerico(numero: number | string, decimales: number = 0)
    {
        /*------------------------------------------------------------------------
		 * Si es String convertimos a Number y limitamos los decimales
		------------------------------------------------------------------------*/
        if(typeof(numero) == "string") numero = parseFloat(numero) ;
        numero = Number(numero).toFixed(decimales);

        /*------------------------------------------------------------------------
		 * Separamos la parte entera con la decimal
		------------------------------------------------------------------------*/
        let numeroString: string = String(numero);
        let numeroArray: string[] = numeroString.split(".");

        let parteEntera = numeroArray[0];
        let parteDecimal = numeroArray[1];

        /*------------------------------------------------------------------------
		 * Iniciamos la salida
		------------------------------------------------------------------------*/
        let salida = "";

        /*------------------------------------------------------------------------
		 * Agregamos las milesimas
		------------------------------------------------------------------------*/
        for(let i=0; i<parteEntera.length; i++)
        {
            if((parteEntera.length - i) % 3 == 0 && i != 0)
            {
                salida += ".";
            }

            salida += parteEntera[i];
        }

        /*------------------------------------------------------------------------
		 * Si hay parte decimal, la agregamos
		------------------------------------------------------------------------*/
        if(parteDecimal != undefined && parteDecimal != null) {
            salida += ","+parteDecimal;
        }

        /*------------------------------------------------------------------------
		 * Retornamos la salida
		------------------------------------------------------------------------*/
        return salida;
    }

    /*============================================================================
	 *
	 *	Boolean to Text
	 *
	============================================================================*/
    public static bool2text(valorBool: boolean)
    {
        /*------------------------------------------------------------------------
		 * TRUE = "Si"
		------------------------------------------------------------------------*/
        if(valorBool)
        {
            return "Si";
        }
        /*------------------------------------------------------------------------
		 * FALSE = "No"
		------------------------------------------------------------------------*/
        else
        {
            return "No";
        }
    }
}