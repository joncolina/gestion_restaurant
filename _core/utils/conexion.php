<?php

/*================================================================================
 *--------------------------------------------------------------------------------
 *
 *	Conexion
 *
 *--------------------------------------------------------------------------------
================================================================================*/
class Conexion
{
    /*============================================================================
	 *
	 *	Atributos
	 *
    ============================================================================*/
    private static $mysql;
    private static $sqlite;
    
    /*============================================================================
	 *
	 *	Iniciar
	 *
    ============================================================================*/
    public static function Iniciar()
    {
        try {
            self::$mysql = new MySQL(BD_SERVIDOR, BD_PUERTO, BD_USUARIO, BD_CLAVE, BD_NOMBRE);
        } catch(Exception $e) {
            throw new Exception("Error en la conexion MySQL.");
        }
    }

    public static function IniciarSQLite($ruta)
    {
        self::$sqlite = new SQLite($ruta);
    }
    
    /*============================================================================
	 *
	 *	Getter
	 *
    ============================================================================*/
    public static function getMysql() {
        return self::$mysql;
    }

    public static function getSqlite() {
        return self::$sqlite;
    }
}