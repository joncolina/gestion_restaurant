    ============================================================================*/
    public function index()
    {
        $respuesta = [];
        $respuesta['status'] = TRUE;
        $respuesta['mensaje'] = "";
        $respuesta['data'] = [];
        Sesion::Cerrar();

        if( Peticion::getEsAjax() ) {
            echo json_encode( $respuesta );
        } else {
            header("location: ".HOST_GERENCIAL."Login/");
        }
    }
